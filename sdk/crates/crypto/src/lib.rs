/// Canonical canonicalization of the per-object chunk AAD payload as
/// produced by [`ChunkAadFormat::Canonical`]. Exposed so callers
/// (e.g. the api-client crate) can produce the same bytes for
/// signing / display purposes without re-implementing the joiner.
pub fn canonical_chunk_aad(
    tenant_id: &str,
    bucket: &str,
    object_key_hash_hex: &str,
    version_id: &str,
) -> Vec<u8> {
    let mut out = Vec::with_capacity(
        tenant_id.len() + bucket.len() + object_key_hash_hex.len() + version_id.len() + 3,
    );
    out.extend_from_slice(tenant_id.as_bytes());
    out.push(b'|');
    out.extend_from_slice(bucket.as_bytes());
    out.push(b'|');
    out.extend_from_slice(object_key_hash_hex.as_bytes());
    out.push(b'|');
    out.extend_from_slice(version_id.as_bytes());
    out
}
